#include<stdio.h>

//取一个整数的后四位
int main()
{
	int n = 0;

	printf("请输入一份八进制数 : ");
	scanf("%o", &n);

	printf("结果是：%#o\n", n & (~(~0 << 4)));

	return 0;
}
