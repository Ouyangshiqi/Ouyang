#include<stdio.h>

//使用指针的指针输出字符串
int main()
{
	char * arr[5] = {"赵XX", "钱XX", "孙XX", "李XX", "周XX"};
	char *(*p) = &arr[0];

	printf("%s\n%s\n%s\n%s\n%s\n", p[0], p[1], p[2], p[3], p[4]);
	return 0;
}
